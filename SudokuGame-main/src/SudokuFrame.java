    private void solutionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_solutionActionPerformed
        // TODO add your handling code here:
         for(int i=0;i<9;i++)
            {
                for(int j=0;j<9;j++)
                {   
                    Integer tempVar=correctBoard[i][j];
                    
                        String btnVal=tempVar.toString();
                        btnarr[i][j].setText(btnVal);
                        btnarr[i][j].setBackground(Color.WHITE);
                        btnarr[i][j].setEnabled(false);
                        
                }
            }
    }//GEN-LAST:event_solutionActionPerformed

